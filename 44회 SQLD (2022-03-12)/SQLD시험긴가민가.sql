CREATE TABLE ERRTEST (
    "ID" CHAR(3) PRIMARY KEY,
    "DATE" DATE,
    "NAME" CHAR(10) NOT NULL,
    "CODE" CHAR(3) DEFAULT '000'
);

INSERT INTO  ERRTEST
VALUES(1, SYSDATE, 'AAA', 120);
-- IMPLICITLY CONVERT NUM -> CHAR

INSERT INTO  ERRTEST
VALUES(2, SYSDATE-1, 'BBB', '130');

