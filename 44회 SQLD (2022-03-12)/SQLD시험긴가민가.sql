CCREATE TABLE UTEST1 (
    AAA NUMBER,     ABB NUMBER
);

CREATE TABLE UTEST2 (
    BAA NUMBER,     BBB NUMBER
);

INSERT INTO UTEST1 VALUES (200, 50);

INSERT INTO UTEST2 VALUES (100, 230);


SELECT AAA AS AA, ABB AS AB FROM UTEST1
UNION ALL
SELECT BAA AS BA, BBB AS BA FROM UTEST2
ORDER BY 1, 2;

-- UNION ALL 에서 정렬 어떻게하든 먼저 쓴 칼럼네임 나옴
